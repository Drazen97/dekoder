function dekoder(lista) {
  var bajtovi = [];
  bajtovi = lista.split(" ");
  var recenica = "";
  var ascii_vrijednost;
  for (var i = 0; i < bajtovi.length; i++) {
    ascii_vrijednost = dekadski(bajtovi[i]);
    recenica += String.fromCharCode(ascii_vrijednost);
  }

  return recenica;
}
function dekadski(binarni) {
  let suma = 0;
  for (var i = 0; i < binarni.length; i++) {
    suma += Math.pow(2, binarni.length - 1 - i) * Number(binarni[i]);
  }
  return suma;
}
